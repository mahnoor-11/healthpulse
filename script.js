// ====== باقی کا JS code ویسا ہی رہے گا ======
const homePage = document.getElementById("home");
const detailPage = document.getElementById("detail");
const diseaseGrid = document.getElementById("grid");
const contentBox = document.getElementById("content");
const searchInput = document.getElementById("search");

// Load saved search
searchInput.value = localStorage.getItem("lastSearch") || "";

// ======================
// Render Cards
// ======================

function renderDiseases(list) {
diseaseGrid.innerHTML = "";
list.forEach(disease => {
const card = document.createElement("div");
card.className = "card";
card.innerHTML = `
<div class="icon">${disease.icon}</div>
<h3>${disease.urdu}</h3>
<p>${disease.name}</p>
${disease.category ? `<span class="badge">${disease.category}</span>` : ""}
`;
card.addEventListener("click", () => {
openDisease(disease);
});
diseaseGrid.appendChild(card);
});
}

// ======================
// Open Disease
// ======================

function openDisease(disease) {
homePage.classList.remove("active");
detailPage.classList.add("active");
window.scrollTo({top: 0, behavior: "smooth"});

contentBox.innerHTML = `
<div class="section">
<h2>${disease.icon} ${disease.urdu}</h2>
<p><strong>${disease.name}</strong></p>

<h3>یہ کیا ہے؟</h3>
<p>${disease.what || "معلومات جلد شامل کی جائیں گی۔"}</p>

<h3>علامات</h3>
<ul>
${(disease.symptoms || ["معلومات جلد شامل کی جائیں گی"]).map(item => `<li>${item}</li>`).join("")}
</ul>

<h3>⚠ خطرے کی علامات</h3>
<div class="alert">
${(disease.danger || ["معلومات جلد شامل کی جائیں گی"]).join("<br>• ")}
</div>

<h3>گھریلو دیکھ بھال</h3>
<ul>
${(disease.homeCare || ["معلومات جلد شامل کی جائیں گی"]).map(item => `<li>${item}</li>`).join("")}
</ul>

<h3>ڈاکٹر سے کب ملیں؟</h3>
<ul>
${(disease.doctor || ["معلومات جلد شامل کی جائیں گی"]).map(item => `<li>${item}</li>`).join("")}
</ul>

<h3>بچاؤ</h3>
<ul>
${(disease.prevention || ["معلومات جلد شامل کی جائیں گی"]).map(item => `<li>${item}</li>`).join("")}
</ul>

<div class="footerwarn">
This website provides general health education only.
It does NOT replace medical advice.
Always consult a qualified doctor,
Lady Health Worker, or visit your nearest
BHU/RHC for diagnosis and treatment.
Do not self-medicate.
</div>
</div>
`;
}

// ======================
// Back Button
// ======================
function goHome() {
detailPage.classList.remove("active");
homePage.classList.add("active");
window.scrollTo({top: 0, behavior: "smooth"});
}

// ======================
// Search
// ======================
searchInput.addEventListener("input", function () {
const keyword = this.value.toLowerCase();
localStorage.setItem("lastSearch", keyword);
const filtered = diseases.filter(disease => {
return (
disease.name.toLowerCase().includes(keyword) ||
disease.urdu.includes(keyword)
);
});
renderDiseases(filtered);
});

// Initial Load
renderDiseases(diseases);